package my.practice.code.leetcode.easy.list.base;

public class ListNode {
    public int val = -1;
    public ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
